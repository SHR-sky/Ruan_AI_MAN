# ===== 对话生成器 =====
def generate_conversation(style_name, attraction, q_template):
    """生成单条对话记录"""
    system_content = SYSTEM_PROMPTS[style_name]
    user_content = q_template.format(name=attraction)

    if style_name == "热情本地通":
        assistant_content = _style_local(attraction, q_template)
    elif style_name == "博学文史官":
        assistant_content = _style_learned(attraction, q_template)
    else:
        assistant_content = _style_cute(attraction, q_template)

    return {
        "messages": [
            {"role": "system", "content": system_content},
            {"role": "user", "content": user_content},
            {"role": "assistant", "content": assistant_content},
        ]
    }


def main():
    styles = ["热情本地通", "博学文史官", "萌趣卡通"]
    style_files = {
        "热情本地通": "enthusiastic_local_guide.jsonl",
        "博学文史官": "learned_history_scholar.jsonl",
        "萌趣卡通": "cute_cartoon.jsonl",
    }

    for style in styles:
        filename = style_files[style]
        filepath = f"E:\\AI_MAN\\finetune_dataset\\{filename}"
        records = []

        for attraction in ATTRACTIONS:
            for q_template in QUESTION_TEMPLATES:
                conv = generate_conversation(style, attraction, q_template)
                records.append(conv)

        random.shuffle(records)

        with open(filepath, "w", encoding="utf-8") as f:
            for record in records:
                f.write(json.dumps(record, ensure_ascii=False) + "\n")

        print(f"[OK] {filename} — {len(records)} 条对话记录")


if __name__ == "__main__":
    main()
